/* <----Optional Parameters----->

These are denoted by ? in front of parameters. once assigned those parameters are not
compulsory required to mention thier value.    

*/

function alpha(a:string,b?:string){
    return a+" "+b;
}

let result=alpha("Naveed");
let result1=alpha("Naveed","Khalid");

console.log(result);
console.log(result1);





